// Function to get category counts from any tools array (local or GitHub API)
export const calculateCategoryCounts = (tools) => {
  const categoryCounts = {};
  const unmatchedCategories = new Set();
  
  // Initialize counts
  toolCategories.forEach(category => {
    categoryCounts[category.id] = 0;
  });
  
  // Count tools for each category
  tools.forEach(tool => {
    const toolCategoryId = tool.category.toLowerCase().replace(/\s+/g, '-');
    if (categoryCounts.hasOwnProperty(toolCategoryId)) {
      categoryCounts[toolCategoryId]++;
    } else {
      unmatchedCategories.add(tool.category);
      // Default to Network Security for unmatched categories
      categoryCounts['network-security']++;
    }
  });
  
  // Log unmatched categories for debugging
  if (unmatchedCategories.size > 0) {
    console.log('⚠️ Unmatched tool categories (defaulting to Network Security):', Array.from(unmatchedCategories).join(', '));
  }
  
  return categoryCounts;
};

